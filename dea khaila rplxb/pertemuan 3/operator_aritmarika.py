x = 5
y = 3

# Penjumlahan
print("Penjumlahan =", x + y)
# Pengurangan
print("Pengurangan =", x - y)
# Perkalian
print("perkalian =", x * y)
# Pembagian
print("pembagian =", x / y)
# modulus
print("modulus =", x % y)
# perpangkatan 
print("perpangkatan =", x ** y)
# pembagian bulat
print("pembagian bulat =", x // y)

